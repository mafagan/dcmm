package dcmm;

public class DcmmStatus {
    private int status;
    private long numCachedMsgs;
    private float avgSendingRate;
    private float curAllocatedRate;

    public DcmmStatus(int status, long numCachedMsgs,
            float avgSendingRate, float curAllocatedRate) {
        this.status = status;
        this.numCachedMsgs = numCachedMsgs;
        this.avgSendingRate = avgSendingRate;
        this.curAllocatedRate = curAllocatedRate;
    }

    public DcmmStatus() {}

    public int getStatus() {
        return this.status;
    }

    public long getNumCachedMsgs() {
        return this.numCachedMsgs;
    }

    public float getAvgSendingRate() {
        return this.avgSendingRate;
    }

    public float getCurAllocatedRate() {
        return this.curAllocatedRate;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    public void setNumCachedMsgs(long numCachedMsgs) {
        this.numCachedMsgs = numCachedMsgs;
    }

    public void setAvgSendingRate(float avgSendingRate) {
        this.avgSendingRate = avgSendingRate;
    }

    public void setCurAllocatedRate(float curAllocatedRate) {
        this.curAllocatedRate = curAllocatedRate;
    }
}


